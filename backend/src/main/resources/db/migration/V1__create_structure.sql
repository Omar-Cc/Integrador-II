-- Especificaciones técnicas estructuradas de productos (para modo experto y RAG)
CREATE TABLE especificaciones_producto (
    id_especificacion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_producto INTEGER NOT NULL,
    clave VARCHAR(100) NOT NULL,
    valor TEXT NOT NULL,
    CONSTRAINT fk_especificaciones_productos
        FOREIGN KEY (id_producto) REFERENCES productos (id_producto) ON DELETE CASCADE,
    -- Evitar duplicidad de la misma clave de especificación en un producto
    CONSTRAINT uq_especificaciones_producto_clave UNIQUE (id_producto, clave)
);

-- Relaciones de compatibilidad y venta cruzada
CREATE TABLE relaciones_producto (
    id_relacion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_producto_origen INTEGER NOT NULL, -- Producto asociado (ej. electrodo, boquilla)
    id_producto_destino INTEGER NOT NULL, -- Producto principal (ej. máquina soldadora)
    tipo_relacion VARCHAR(50) NOT NULL,
    CONSTRAINT fk_relaciones_producto_origen
        FOREIGN KEY (id_producto_origen) REFERENCES productos (id_producto) ON DELETE CASCADE,
    CONSTRAINT fk_relaciones_producto_destino
        FOREIGN KEY (id_producto_destino) REFERENCES productos (id_producto) ON DELETE CASCADE,
    CONSTRAINT chk_relaciones_producto_tipo CHECK (tipo_relacion IN (
        'CONSUMIBLE_DE',       -- Insumos consumibles directos (ej. soldadura para máquina)
        'REPUESTO_DE',          -- Partes o repuestos de repuesto (ej. boquilla para antorcha)
        'COMPLEMENTARIO_DE',    -- Accesorios complementarios (ej. máscara para máquina)
        'SUSTITUTO_DE',         -- Producto alternativo / sustituto por falta de stock
        'FRECUENTE_CONJUNTO'    -- Productos que se suelen comprar juntos
    )),
    -- Evita duplicidad de la misma relación dirigida
    CONSTRAINT uq_relaciones_producto_asociacion UNIQUE (id_producto_origen, id_producto_destino, tipo_relacion)
);

